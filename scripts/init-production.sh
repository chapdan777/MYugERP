#!/bin/bash

# Скрипт первоначальной настройки базы данных для Production
# Выполняет накатывание миграций и запуск seed-скриптов

echo "==================================================="
echo "🚀 Инициализация базы данных MYugERP (Production) "
echo "==================================================="

# Переход в директорию бэкенда
cd "$(dirname "$0")/../erp-server" || exit 1

echo "📦 1. Установка зависимостей..."
npm install --production=false

echo "🏗️  2. Сборка проекта..."
npm run build

echo "🗄️  3. Запуск миграций базы данных..."
npm run migration:run

echo "🌱 4. Запуск Seed-скриптов (Наполнение базовыми данными)..."
# Этот скрипт автоматически запустит:
# - initial-seed.ts (Базовые данные)
# - work-order-statuses-seed.ts (Статусы ЗН)
# - requested-properties-seed.ts (Базовые свойства)
# - facade-bom-setup.seed.ts (Универсальный фасад и его свойства)
npm run seed

echo "==================================================="
echo "✅ База данных успешно инициализирована и готова к работе!"
echo "Теперь вы можете запустить сервер командой: npm run start:prod"
echo "==================================================="
